package variableStudy;

public class NumericAndNondecimal {

	public static void main(String[] args) {
     
		byte a; 
		a=123;
		System.out.println("my number is "+a);
		
		short b;
		b=12345;
		System.out.println("my number is "+b);
		
		int c;
		c=1234567891;
		System.out.println("my number is "+c);
		
		long d;
		d=1234567891234567891l;
		System.out.println("my number is "+d);
	}

}
