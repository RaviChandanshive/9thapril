package loopStudy;

public class DoWhileLoop {

	public static void main(String[] args) {
		int a=5;
       do {
    	   System.out.println(a);
		a=a+5;
	} while (a<=50);
       System.out.println("=============");
       
       int b=50;
       do {
		System.out.println(b);
		b=b-5;
	} while (b>=5);
       
	}

}
