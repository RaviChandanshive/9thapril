package loopStudy;

public class WhileLoopStudy {

	public static void main(String[] args) {
		
		int a=2;
		
		while (a<=20) {
			System.out.println(a);
			 
			a=a+2;
		}   System.out.println("===============");
        int b=20;
        
        while (b>=2) {
			System.out.println(b);
			b=b-2;
		}
	}
	
	

}
